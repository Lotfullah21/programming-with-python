# String
b = "hello"
print(type(b))
# List
newList = [1,2,3,4,"10"]
print(type(newList))
# Tuple
newTuple = (1,2,3,4,5)
newTuple = newTuple + (12,10,)
print(type(newTuple))
# Set
newSet = {1,2,3}
print(type(newSet))
newSet = set([1, 2, 3])
print(type(newSet))
print(newSet)
# Dictionary
a = {1:"1",b:"12"}
print(type(a))
print(a)